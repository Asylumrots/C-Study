#include <stdio.h>

int main04()
{
	//野指针--->指针变量指向一个未知的空间
	int* p = 100;
	//程序中允许存在野指针
	//不建议将一个值直接赋值给指针
	//操作系统将0-255作为系统占用不允许访问操作
	//对应的内存空间可能报错
	printf("%d", *p);

	//空指针是指内存地址为0的空间
	int* p2 = NULL;//#define NULL ((void *)0)

	//操作空指针对应的空间一定会报错
	printf("%d", *p2);

	//空指针可用在条件判断
	if (p2==NULL)
	{

	}

	return 0;
}